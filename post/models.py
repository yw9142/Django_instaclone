from django.conf import settings
from django.db import models
from imagekit.models import ProcessedImageField
from imagekit.processors import ResizeToFill


def photo_path(instance, filename):
    from time import strftime
    from random import choice
    import string

    arr = [choice(string.ascii_letters) for _ in range(8)]
    pid = ''.join(arr)
    extension = filename.split('.')[-1]
    return f"{strftime('post/%Y/%m/%d/')}/{instance.author.username}/{pid}.{extension}"

    # return '{}/{}/{}.{}'.format(strftime('post/%Y/%m/%d/'),
    #                             instance.author.username, pid, extension)


class Post(models.Model):
    author = models.ForeignKey(settings.AUTH_USER_MODEL,
                               on_delete=models.CASCADE)
    photo = ProcessedImageField(upload_to=photo_path, processors=[
        ResizeToFill(600, 600)], format='JPEG', options={'quality': 90})

    content = models.CharField(max_length=140, help_text="최대 140자 입력 가능")
    created_at = models.DateTimeField(auto_now_add=True)
    updated_at = models.DateTimeField(auto_now=True)

    # 생성 시간 기준으로 정렬
    class Meta:
        ordering = ['-created_at']

    def __str__(self):
        return self.content
